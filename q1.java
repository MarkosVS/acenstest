class Q1 {
	public static void main (String[] args){
		// declaração das variáveis x e y
		int x = 2;
		int y = 5;
		// início da estrutura condicional
		if(y > 8){
			y = y * 2;
		}
		else{
			x = x * 2;
		}
		// imprime na tela o resultado da soma das variaveis
		System.out.print(x+y);
	}
}